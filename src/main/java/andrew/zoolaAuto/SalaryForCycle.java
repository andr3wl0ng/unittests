package andrew.zoolaAuto;

public class SalaryForCycle {
    public static void main(String[] args) {
        int currentSalary=0;
        for (int i = 0; i < 12; i++) {
            currentSalary=currentSalary+1000;
            System.out.println("my current salary is "+ currentSalary);
        }
    }
}

