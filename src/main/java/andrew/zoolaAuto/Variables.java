package andrew.zoolaAuto;

public class Variables {
    public static void main(String [] args){

        int myInt = 557;
        short myShort = 3266;  // 16bit numbers; from -32000 to 32000
        long myLong = 555222555; //64bit info
        double myDouble = 5.4; // 64bit
        float myFloat = 522.5f; //32bit
        char myChar = 'a';
        boolean myBool = true;
        byte myByte = 127; // 1bit info from -128 to 127

        System.out.println(myBool);
        System.out.println(myByte);
        System.out.println(myChar);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myInt);
        System.out.println(myLong);
        System.out.println(myShort);

    }
}
